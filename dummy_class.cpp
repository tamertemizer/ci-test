/**
* @brief Adds two integers
* @param x Num 1 
* @param y Num 2
* @return sum of two ints
*/

int addition(int x, int y)
{
	return (x + y);
}


/**
* @brief Multiplies two integers
* @param x Num 1
* @param y Num 2
* @return product of two ints
*/

int multiply(int x, int y)
{
	return (x * y);
}

